"""
IO Dataset for Bi-LSTM training.

Loads trajectories generated by traj_gen_v4.py.
Window=100, stride=50 (train), stride=100 (inference/eval).
Output target: body-frame relative pose at start of window.
"""

import os
from glob import glob

import numpy as np
import torch
from torch.utils.data import Dataset
from scipy.spatial.transform import Rotation as R


# Trajectory NPZ column indices (matches traj_gen_v4 TRAJ_COLUMNS)
TRAJ_T = 0
TRAJ_POS = slice(1, 4)        # x, y, z
TRAJ_QUAT = slice(10, 14)     # qw, qx, qy, qz

# IMU NPZ column indices (matches traj_gen_v4 IMU_COLUMNS)
IMU_ACCEL = slice(1, 4)       # accel_x, accel_y, accel_z (noisy measured)
IMU_GYRO = slice(4, 7)        # gyro_x, gyro_y, gyro_z   (noisy measured)


def quat_wxyz_to_R(q_wxyz: np.ndarray) -> np.ndarray:
    """Convert wxyz quaternion to 3x3 rotation matrix.
    Accepts shape (4,) or (N, 4). Returns (3,3) or (N,3,3).
    """
    if q_wxyz.ndim == 1:
        q_xyzw = np.array([q_wxyz[1], q_wxyz[2], q_wxyz[3], q_wxyz[0]])
    else:
        q_xyzw = np.concatenate([q_wxyz[:, 1:4], q_wxyz[:, 0:1]], axis=1)
    return R.from_quat(q_xyzw).as_matrix()


def compute_relative_pose(p_start: np.ndarray, q_start: np.ndarray,
                          p_end: np.ndarray, q_end: np.ndarray):
    """Compute body-frame relative pose from start frame to end frame.

    Returns:
        delta_p_body: (3,) translation in body frame at start
        delta_R_6d:   (6,) first two columns of delta_R, flattened
    """
    R_start = quat_wxyz_to_R(q_start)
    R_end = quat_wxyz_to_R(q_end)

    # Translation in body frame at start
    delta_p_body = R_start.T @ (p_end - p_start)

    # Rotation in body frame at start
    delta_R = R_start.T @ R_end

    # 6D representation: first two columns of delta_R
    # delta_R[:, 0] is column 0 (3 numbers), delta_R[:, 1] is column 1
    delta_R_6d = np.concatenate([delta_R[:, 0], delta_R[:, 1]])

    return delta_p_body.astype(np.float32), delta_R_6d.astype(np.float32)


def normalize_imu(imu: np.ndarray) -> np.ndarray:
    """Apply hardcoded normalization.

    accel_z: (val - 10.5) / 1.5
    Other 5 channels: pass through.

    Input: (T, 6) where channels are [accel_x, accel_y, accel_z, gyro_x, gyro_y, gyro_z]
    """
    out = imu.copy().astype(np.float32)
    out[:, 2] = (out[:, 2] - 10.5) / 1.5
    return out


class IODataset(Dataset):
    """Window-based dataset for IO Bi-LSTM training.

    Loads all trajectories from `split_dir`, extracts windows of size
    `window_size` with given `stride`, computes per-window targets in
    body frame, applies input normalization.
    """

    def __init__(self, split_dir: str, window_size: int = 100, stride: int = 50):
        self.window_size = window_size
        self.stride = stride
        self.windows = []           # list of dicts (lazy on tensors)

        traj_folders = sorted(glob(os.path.join(split_dir, 'traj_*')))
        if len(traj_folders) == 0:
            raise FileNotFoundError(f"No traj_* folders found under {split_dir}")

        for folder in traj_folders:
            self._load_trajectory(folder)

        if len(self.windows) == 0:
            raise RuntimeError(f"No windows extracted from {split_dir}")

    def _load_trajectory(self, folder: str):
        traj_npz = np.load(os.path.join(folder, 'trajectory.npz'))
        imu_npz = np.load(os.path.join(folder, 'imu.npz'))
        traj = traj_npz['data']    # (N, 20)
        imu = imu_npz['data']      # (N, 13)

        N = len(traj)
        assert len(imu) == N, f"Length mismatch in {folder}: traj={N}, imu={len(imu)}"

        pos = traj[:, TRAJ_POS]            # (N, 3)
        quat = traj[:, TRAJ_QUAT]          # (N, 4) wxyz
        imu_accel = imu[:, IMU_ACCEL]      # (N, 3) noisy
        imu_gyro = imu[:, IMU_GYRO]        # (N, 3) noisy
        imu_6 = np.concatenate([imu_accel, imu_gyro], axis=1)  # (N, 6)

        W = self.window_size
        S = self.stride

        for k_start in range(0, N - W + 1, S):
            k_end = k_start + W - 1   # inclusive last sample of window

            imu_window = imu_6[k_start:k_start + W]   # (W, 6)
            imu_window = normalize_imu(imu_window)

            delta_p, delta_R_6d = compute_relative_pose(
                p_start=pos[k_start], q_start=quat[k_start],
                p_end=pos[k_end],   q_end=quat[k_end],
            )

            self.windows.append({
                'imu': imu_window,
                'delta_p': delta_p,
                'delta_R_6d': delta_R_6d,
                'traj_folder': folder,
                'k_start': k_start,
            })

    def __len__(self):
        return len(self.windows)

    def __getitem__(self, idx):
        w = self.windows[idx]
        return {
            'imu': torch.from_numpy(w['imu']),
            'delta_p': torch.from_numpy(w['delta_p']),
            'delta_R_6d': torch.from_numpy(w['delta_R_6d']),
        }


def load_trajectory_for_eval(folder: str, window_size: int = 100, stride: int = 100):
    """Load a single trajectory for sequential dead-reckoning eval.

    Returns dict with:
        windows_imu:   (M, W, 6) normalized IMU windows
        gt_pos:        (N, 3) full GT positions
        gt_quat_wxyz:  (N, 4) full GT quaternions
        t:             (N,) timestamps
        window_starts: (M,) starting sample index of each window
        window_size: W
    """
    traj_npz = np.load(os.path.join(folder, 'trajectory.npz'))
    imu_npz = np.load(os.path.join(folder, 'imu.npz'))
    traj = traj_npz['data']
    imu = imu_npz['data']
    N = len(traj)

    t = traj[:, TRAJ_T]
    pos = traj[:, TRAJ_POS]
    quat = traj[:, TRAJ_QUAT]
    imu_accel = imu[:, IMU_ACCEL]
    imu_gyro = imu[:, IMU_GYRO]
    imu_6 = np.concatenate([imu_accel, imu_gyro], axis=1)

    W = window_size
    S = stride

    windows_imu = []
    window_starts = []
    for k_start in range(0, N - W + 1, S):
        wnd = normalize_imu(imu_6[k_start:k_start + W])
        windows_imu.append(wnd)
        window_starts.append(k_start)

    return {
        'windows_imu': np.array(windows_imu, dtype=np.float32),     # (M, W, 6)
        'gt_pos': pos.astype(np.float64),
        'gt_quat_wxyz': quat.astype(np.float64),
        't': t.astype(np.float64),
        'window_starts': np.array(window_starts, dtype=np.int64),
        'window_size': W,
        'folder': folder,
    }
